// Task 7: TreeNode with value, left, right

class TreeNode {
    constructor(value) {
      this.value = value;
      this.left = null;
      this.right = null;
    }
  }
  
  // Example usage:
  const rootNode = new TreeNode(5);
  console.log(rootNode); // Output: TreeNode { value: 5, left: null, right: null }
  